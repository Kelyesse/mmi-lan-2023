                <h2 class="h2">4. Conduites et comportements</h2>
            </div>
            <div>
                <div>
                    <h3 class="h3">4.1. Respect et Fair-Play</h3>
                    <p class="texte">Les participants sont priés de suivre strictement les règles de politesse et de fair-play en toutes circonstances. Tout comportement, tel que du harcèlement, de l'intimidation ou toute forme de conduite inappropriée, sera formellement interdit et entraînera un avertissement. Après deux avertissements, une disqualification immédiate et une potentielle expulsion de l'événement seront appliquées. Nous comptons sur la compréhension et la coopération de tous pour créer un environnement respectueux et équitable.</p>
                </div>
                <div>
                    <h4 class="h4">4.1.1  Inscription Obligatoire Généralités</h4>
                    <p class="texte">Il est impératif que tous les participants utilisent un langage respectueux envers les autres joueurs, les organisateurs et le personnel de l'événement. L'usage de tout langage offensant, discriminatoire ou injurieux est strictement interdit. Tout manquement au respect de l'utilisation d'un langage approprié entraînera des sanctions sévères. Cela pourrait inclure des pénalités de points, voire une exclusion totale de l'événement, selon la gravité de l'infraction. Nous sommes fermement engagés à maintenir un environnement respectueux et positif, et les sanctions seront appliquées de manière équitable pour assurer le bien-être de tous les participants.</p>
                </div>
                <div>
                    <h4 class="h4">4.1.2  Inscription Obligatoire Généralités</h4>
                    <p class="texte">Une attitude sportive est attendue de tous les participants, quel que soit le résultat des matchs. Les manifestations d'hostilité envers les autres participants, qu'elles soient physiques ou verbales, sont strictement interdites.</p>
                </div>
                <div>
                    <h4 class="h4">4.1.3  Inscription Obligatoire Généralités</h4>
                    <p class="texte">Dans le cadre festif de l'événement, les participants sont priés de porter une tenue décente. Les tenues provocantes, offensantes ou inappropriées ne seront pas tolérées. L'organisateur se réserve le droit d'exclure tout participant ne respectant pas cette règle, afin de maintenir une atmosphère respectueuse et appropriée pour tous les participants.</p>
                </div>
                <div>
                    <h3 class="h3">4.2. Alcool et Drogues</h3>
                    <p class="texte">L'alcool et les drogues sont formellement interdits sur le site de l'événement. Tout participant en état d'ébriété ou faisant usage de substances illicites sera immédiatement exclu de la compétition.</p>
                </div>
                <div>
                    <h3 class="h3">4.3. Consommation de Nourriture et Boissons</h3>
                    <p class="texte">La consommation de nourriture et de boissons est autorisée, dans le respect des lieux et des autres participants. Nous vous prions de maintenir l'espace propre et d'utiliser des contenants résistants aux liquides afin de préserver la propreté et la sécurité générale.</p>
                </div>
                <div>
                    <h3 class="h3">4.4. Respect des Horaires</h3>
                    <p class="texte">Il est impératif que les participants observent scrupuleusement les horaires prévus pour les matchs et les activités associées. Tout retard non justifié compromet le bon déroulement de l'événement et peut entraîner des sanctions, voire la disqualification de l'équipe. Afin de maintenir l'équité et la fluidité de la compétition, les sanctions suivantes pourraient être appliquées en cas de non-respect des horaires :</p>
                </div>
                <div>
                    <h4 class="h4">Avertissement officiel </h4>
                    <p class="texte">Un premier retard peut entraîner un avertissement officiel adressé à l'équipe.</p>
                </div>
                <div>
                    <h4 class="h4">Pénalités de points </h4>
                    <p class="texte">Des points peuvent être déduits du score de l'équipe en retard, impactant ainsi sa position dans le classement général.</p>
                </div>
                <div>
                    <h4 class="h4">Forfait pour le match</h4>
                    <p class="texte">L'équipe en retard peut être considérée comme ayant perdu le match en question, ce qui influe sur son parcours dans la compétition.</p>
                </div>
                <div>
                    <h4 class="h4">Disqualification totale</h4>
                    <p class="texte">En cas de non-respect persistant des horaires, l'équipe peut être disqualifiée de l'événement dans son intégralité.</p>
                </div>
                <div>
                    <h3 class="h3">4.5. Respect des Équipements</h3>
                    <p class="texte">Les participants sont responsables du matériel mis à leur disposition. Tout acte de vandalisme ou de détérioration volontaire des équipements de l'événement sera sanctionné et pourrait entraîner des poursuites judiciaires.</p>
                </div>
