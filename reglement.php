                    <h4 class="h4">Remboursement des dommages</h4>
                    <p class="texte">Les participants peuvent être tenus financièrement responsables et contraints de rembourser les coûts liés à la réparation ou au remplacement des équipements endommagés.</p>
                </div>
                <div>
                    <h4 class="h4">Exclusion totale</h4>
                    <p class="texte">Pour des actes répétés ou de vandalisme majeur, l'équipe ou le participant peut être disqualifié de l'événement dans son ensemble.</p>
