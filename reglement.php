            <div>
                <h2 class="h2">2. Conditions et phases d'inscriptions</h2>
            </div>
            <div>
                <div>
                    <h3 class="h3">2.1 Inscriptions Obligatoire Généralités</h3>
                    <p class="texte">Toute personne désirant participer à la LAN doit s'inscrire sur le site : nomsite. De plus, pour pouvoir profiter des facilités de covoiturages présents sur le site, vous devrez obligatoirement faire partie des inscrits.</p>
                </div>
                <div>
                    <h3 class="h3">2.2 Formation d'Équipe</h3>
                    <p class="texte">Pour constituer une équipe, chaque membre doit s'inscrire individuellement sur le site, puis rejoindre l'équipe créée par l'un des membres. Le créateur d'équipe doit approuver la demande d'adhésion des autres membres.</p>
                </div>
                <div>
                    <h3 class="h3">2.3 Dissolution d'Équipe</h3>
                    <p class="texte">Pour supprimer son compte, un participant doit préalablement quitter son équipe s'il en fait partie, ou la dissoudre s'il en est le créateur.</p>
                </div>
                <div>
                    <h3 class="h3">2.4 Composition d'Équipe</h3>
                    <p class="texte">Une équipe doit impérativement être composée de 3 membres.</p>
                </div>
                <div>
                    <h3 class="h3">2.5 Rappel Avant l'Événement</h3>
                    <p class="texte">Deux semaines avant l'événement, le 29 novembre 2023, un rappel sera envoyé aux participants inscrits dans une équipe incomplète ou sans équipe, les encourageant à finaliser leur équipe ou à en rejoindre une existante avant la clôture des inscriptions (29 novembre 23:59).</p>
                </div>
                <div>
                    <h3 class="h3">2.6 Requalification en Spectateur</h3>
                    <p class="texte">Deux semaines avant la LAN, le 29 novembre 2023, 23:59, si une équipe demeure incomplète ou si un participant n'a pas encore rejoint une équipe, ces individus seront automatiquement requalifiés en tant que spectateurs, excluant ainsi leur participation active. Ils restent néanmoins les bienvenus à la LAN et peuvent toujours profiter des options de covoiturage.</p>
                </div>
            </div>
        </div>
