                <h2 class="h2">9. Droit à l'image</h2>
            </div>
            <div>
                <div>
                    <h3 class="h3">9.1. Consentement à la Captation d'Images</h3>
                    <p class="texte">En participant à l'événement, les participants donnent leur consentement à la captation d'images, incluant des photos et des vidéos, qui peuvent être utilisées à des fins promotionnelles.</p>
                </div>
                <div>
                    <h3 class="h3">9.2. Droit de Retrait </h3>
                    <p class="texte">Si un participant découvre une image ou une vidéo de lui-même qui le dérange après l’événement, il a le droit de faire part de sa demande de retrait à l’organisateur. Ce dernier s'engage à faire de son mieux pour retirer ou flouter l’image en question, dans la mesure du possible. Il est important de préciser que cet engagement de retrait s'applique uniquement aux images capturées par les organisateurs de l'événement. Les photos ou vidéos prises par des tierces personnes durant l'événement, sur lesquelles l'organisateur n'a pas de contrôle, ne relèvent pas de cette responsabilité.</p>
                </div>
                <div>
                    <h3 class="h3">9.3. Consentement à l'Enregistrement en Direct</h3>
                    <p class="texte">Les participants acceptent que leur image puisse être capturée et diffusée en temps réel si des parties de l’événement sont diffusées en direct sur Internet. Il est cependant noté que les vidéos en direct ne seront généralement pas conservées après l’événement. </p>
                </div>
                <div>
                    <h3 class="h3">9.4. Utilisation Responsable</h3>
                    <p class="texte">L'organisateur garantit formellement que les images et enregistrements seront utilisés de manière responsable, assurant ainsi qu'aucun préjudice ne sera causé aux participants et que leur vie privée sera rigoureusement préservée.</p>
                </div>
                <div>
                    <h3 class="h3">9.5. Contact pour les Questions</h3>
                    <p class="texte">Tout participant ayant des questions ou des préoccupations concernant l’utilisation de son image peut contacter l’organisateur de l’événement pour obtenir des clarifications ou demander des actions spécifiques.</p>
                </div>
