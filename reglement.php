            <div>
                <h2 class="h2">5. Matériels</h2>
            </div>
            <div>
                <div>
                    <h3 class="h3">5.1. Équipement Obligatoire</h3>
                    <p class="texte">Chaque participant est tenu de se présenter à la Lan Party avec son ordinateur personnel complet, incluant clavier, souris, un seul écran, unité centrale, câbles d'alimentation, et une multiprise de type "triplette" ou une multiprise à 5 sorties. Il est impératif que chaque participant dispose d'un antivirus à jour et activé, ainsi que d'un ordinateur scanné et dépourvu de tout logiciel malveillant, assurant ainsi la sécurité du réseau partagé et la qualité de l'expérience de jeu pour tous les participants.</p>
                    <div>
                        <h4 class="h4">5.1.1 Casque Audio Obligatoire :</h4>
                        <p class="texte">L'utilisation de casques audio est obligatoire, les enceintes ne sont pas autorisées afin de préserver la tranquillité de tous les participants.</p>
                    </div>
                </div>
                <div>
                    <h3 class="h3">5.2. Jeux Originaux </h3>
                    <p class="texte">Chaque participant doit disposer de ses propres jeux originaux, et l'utilisation de jeux piratés est strictement interdite.</p>
                </div>
                <div>
                    <h3 class="h3">5.3. Restrictions d'Appareils Électriques </h3>
                    <p class="texte">Il est strictement interdit d'apporter d'autres appareils électriques que ceux mentionnés ci-dessus. Les petits réfrigérateurs, ventilateurs, et tout autre appareil similaire sont proscrits.</p>
                </div>
                <div>
                    <h3 class="h3">5.4. Prêt de Matériel </h3>
                    <p class="texte">En cas de prêt de matériel entre les participants ou au sein de l'équipe, il est impératif de restituer l'objet du prêt avant le départ.</p>
                </div>
                <div>
                    <h3 class="h3">5.5. Responsabilité du Matériel </h3>
                    <p class="texte">Tout matériel apporté demeure sous la responsabilité du participant. Les organisateurs ne peuvent être tenus responsables en cas de perte, de vol ou de détérioration des affaires personnelles. Chaque participant est invité à surveiller, ranger, et protéger son équipement.</p>
                </div>
                <div>
                    <h3 class="h3">5.6. Responsabilité du Contenu Numérique </h3>
                    <p class="texte">Chaque participant est individuellement responsable du contenu de son ordinateur, y compris le contenu numérique de ses disques. En cas de perte de données, les organisateurs ne seront pas tenus responsables. De plus, il est important de noter que les organisateurs ne peuvent en aucun cas être tenus pour responsables du contenu spécifique stocké sur les disques des participants. La responsabilité totale de la gestion et de la préservation du contenu, ainsi que des éventuelles pertes de données, incombe exclusivement à chaque participant.</p>
                </div>
                <div>
                    <h3 class="h3">5.7. Données Informatiques </h3>
                    <p class="texte"> Les organisateurs ne pourront être tenus responsables en cas de perte ou de modification des données informatiques présentes sur les machines des participants.</p>
                </div>
            </div>
        </div>
