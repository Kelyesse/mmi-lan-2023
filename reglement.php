                <h2 class="h2">3. Conditions d'accès à la buvette</h2>
            </div>
            <div>
                <div>
                    <h3 class="h3">3.1 Carte Buvette à l'Arrivée</h3>
                    <p class="texte">À leur arrivée sur le lieu de l’événement, chaque participant recevra une carte buvette. Ce badge individuel servira de moyen de paiement pour les achats à la buvette.</p>
                </div>
                <div>
                    <h3 class="h3">3.2 Avantages de la Carte</h3>
                    <p class="texte">La carte buvette attribuera des points aux participants, leur permettant ainsi de bénéficier de services à la buvette. Ces points seront utilisés comme moyen de transaction pour des rafraîchissements, collations et autres produits disponibles.</p>
