            <div>
                <h2 class="h2">6. Avertissement et charte informatique</h2>
            </div>
            <div>
                <div>
                    <h3 class="h3">6.1. Responsabilité Individuelle</h3>
                    <p class="texte">Vous demeurez l'unique responsable de votre condition physique. Votre participation au tournoi est sous votre propre responsabilité. En cas de problèmes de santé ou d'allergies, nous vous prions de nous en informer avant votre participation à l'événement.</p>
                </div>
                <div>
                    <h3 class="h3">6.2. Partage de Fichiers pendant les Tournois</h3>
                    <p class="texte">Le partage de fichiers pendant les tournois est strictement interdit afin de préserver la qualité du réseau et des temps de réponse. Les transferts de fichiers en dehors des tournois sont autorisés.</p>
                </div>
                <div>
                    <h3 class="h3">6.3. Échanges de Fichiers en dehors des Tournois</h3>
                    <p class="texte">Les échanges de fichiers en dehors des tournois sont tolérés, mais les organisateurs déclinent toute responsabilité quant à la nature des données échangées et stockées sur les machines personnelles des joueurs.</p>
                </div>
                <div>
                    <h3 class="h3">6.4. Utilisation Non Autorisée de l'Accès Internet </h3>
                    <p class="texte">Toute utilisation non autorisée de l'accès Internet (téléchargements, streaming, musique, vidéo, etc.) entraînera une exclusion immédiate du tournoi.</p>
                </div>
                <div>
                    <h3 class="h3">6.5. Connexion au Réseau Wifi de l'Université </h3>
                    <p class="texte">Les participants sont tenus d'utiliser exclusivement le réseau wifi de l'université avec leurs identifiants étudiants. La connexion en dehors du réseau universitaire pendant l'événement est strictement interdite. (a voir avec les dev si on a un plan B quand même si le réseau de la fac part en sucette)</p>
                </div>
                <div>
                    <h3 class="h3">6.6. Restrictions sur le Jeu et la Location en dehors du Réseau Universitaire </h3>
                    <p class="texte">Les participants s'engagent à jouer uniquement entre eux, excluant toute connexion en dehors du réseau universitaire et avec d'autres joueurs que ceux de l'événement.</p>
                </div>
                <div>
                    <h3 class="h3">6.7. Déconnexion des Sessions à la Fin de l'Événement :</h3>
                    <p class="texte">Les participants sont tenus de se déconnecter de leurs sessions à la fin de l'événement pour assurer un déroulement fluide et sécurisé.</p>
                </div>
            </div>
        </div>
