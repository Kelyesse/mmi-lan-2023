            <div>
                <h2 class="h2">7. Sécurités et responsabilités</h2>
            </div>
            <div>
                <div>
                    <h3 class="h3">7.1. Utilisation des Prises Électriques et Rallonges </h3>
                    <p class="texte">Des consignes seront fournies aux participants afin d'éviter de surcharger les prises électriques et les rallonges. Il est impératif de respecter ces règles de sécurité électrique pour garantir le bon déroulement de la LAN et prévenir tout incident.</p>
                </div>
                <div>
                    <h3 class="h3">7.2. Évacuation en Cas d'Urgence </h3>
                    <p class="texte">En situation d'urgence, compte tenu des mesures de sécurité renforcées en vigueur (Vigipirate niveau maximal), il est impératif que tous les participants suivent strictement les consignes d'évacuation fournies par le PC sécurité.</p>
                </div>
                <div>
                    <h3 class="h3">7.3. Signalement Immédiat des Problèmes de Sécurité </h3>
                    <p class="texte">Tout participant identifiant un problème de sécurité doit le signaler immédiatement aux organisateurs ou au PC Sécurité  pour une intervention rapide.</p>
                </div>
                <div>
                    <h3 class="h3">7.4. Respect des Consignes de Sécurité Incendie </h3>
                    <p class="texte">Les participants doivent respecter strictement les consignes de sécurité incendie, y compris l'utilisation appropriée des extincteurs et le respect des sorties de secours.</p>
                </div>
                <div>
                    <h3 class="h3">7.5. Respect des Marquages au Sol </h3>
                    <p class="texte">Le respect des marquages au sol et des zones délimitées est essentiel pour assurer un déplacement sûr dans l'espace de l'événement. Il est impératif de veiller à ne pas obstruer certains passages, notamment ceux définis comme zones à accès restreint, tels que la régie. Toute violation de cette règle peut entraîner une exclusion de l'événement, à la fois pour des raisons de sécurité et pour garantir le bon fonctionnement des zones spécifiques réservées à l'organisation.</p>
                </div>
                <div>
                    <h3 class="h3">7.6. Restrictions sur les Objets Dangereux </h3>
                    <p class="texte">Les objets dangereux tels que lasers, substances inflammables ou tout équipement potentiellement nocif sont strictement interdits.</p>
                </div>
            </div>
        </div>
